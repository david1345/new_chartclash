AS $$
BEGIN
  RETURN QUERY
  SELECT 
    p.asset_symbol as symbol,
    MAX(p.asset_type) as asset_type, -- Mocking asset_type from predictions table structure if needed, or joining with assets table. Assuming consistent type per symbol.
