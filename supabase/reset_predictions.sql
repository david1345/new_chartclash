-- DANGER: This will delete all existing predictions
DROP TABLE IF EXISTS public.predictions CASCADE;

-- Recreate Predictions Table with proper columns and NO unique constraints on user_id
CREATE TABLE public.predictions (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES public.profiles(id) NOT NULL,
  asset_symbol TEXT NOT NULL DEFAULT 'BTCUSDT',
  direction TEXT NOT NULL, -- 'UP' or 'DOWN'
  target_percent NUMERIC NOT NULL DEFAULT 0.5,
  entry_price NUMERIC NOT NULL DEFAULT 0,
  status TEXT DEFAULT 'pending', -- 'pending', 'won', 'lost'
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  resolved_at TIMESTAMP WITH TIME ZONE
);

-- Enable RLS
ALTER TABLE public.predictions ENABLE ROW LEVEL SECURITY;

-- Re-apply Policies
CREATE POLICY "Predictions are viewable by everyone." ON public.predictions
  FOR SELECT USING (true);

CREATE POLICY "Users can insert their own predictions." ON public.predictions
  FOR INSERT WITH CHECK (auth.uid() = user_id);
