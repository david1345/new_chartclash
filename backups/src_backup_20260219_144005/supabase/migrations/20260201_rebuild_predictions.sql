-- 1. Predictions Table (완전히 다시 작성)
-- 기존 테이블이 있다면 DROP (데이터 백업이 필요하다면 별도로 진행해야 함)
drop table if exists public.predictions cascade;

create table public.predictions (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users(id) not null, -- references auth.users directly or profiles
  
  -- 예측 정보
  asset_symbol text not null,
  timeframe text not null,                    -- 15m, 1h, 4h, 1d
  direction text not null check (direction in ('UP', 'DOWN')),
  target_percent numeric not null check (target_percent in (0.5, 1.0, 1.5, 2.0)),
  entry_price numeric not null check (entry_price > 0),
  bet_amount integer not null check (bet_amount > 0 and bet_amount <= 1000), 
  
  -- 결과
  status text default 'pending' check (status in ('pending', 'WIN', 'LOSS', 'ND')),
  actual_price numeric,                       
  profit integer default 0,                   
  
  -- 타임스탬프
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  candle_close_at timestamp with time zone not null, 
  resolved_at timestamp with time zone,
  
  -- 댓글 (게임용 한줄 평)
  comment text check (char_length(comment) <= 140) 
);

-- 성능 최적화 인덱스
create index idx_predictions_status on predictions(status);
create index idx_predictions_user on predictions(user_id);
create index idx_predictions_candle on predictions(asset_symbol, timeframe, candle_close_at);
create index idx_predictions_created on predictions(created_at desc);

-- RLS
alter table public.predictions enable row level security;

drop policy if exists "Predictions are viewable by everyone." on public.predictions;
create policy "Predictions are viewable by everyone." on public.predictions
  for select using (true);

drop policy if exists "Users can insert their own predictions." on public.predictions;
create policy "Users can insert their own predictions." on public.predictions
  for insert with check (auth.uid() = user_id);

-- 2. ⭐⭐⭐ 핵심 RPC 함수 추가 (resolve_prediction_advanced) ⭐⭐⭐
drop function if exists public.resolve_prediction_advanced(bigint, numeric);

create or replace function public.resolve_prediction_advanced(
    p_id bigint,
    p_close_price numeric
)
returns json
language plpgsql
security definer
as $$
declare
    v_prediction record;
    v_price_change numeric;
    v_price_change_percent numeric;
    v_status text;
    v_payout integer := 0;
    v_result json;
begin
    -- 1. 예측 조회 (Lock으로 동시성 제어)
    select * into v_prediction
    from predictions
    where id = p_id and status = 'pending'
    for update;
    
    if not found then
        return json_build_object(
            'success', false,
            'error', 'Prediction not found or already resolved'
        );
    end if;
    
    -- 2. 가격 변화 계산
    v_price_change := p_close_price - v_prediction.entry_price;
    v_price_change_percent := abs(v_price_change / v_prediction.entry_price * 100);
    
    -- 3. 승패 판정 로직
    if v_prediction.direction = 'UP' then
        -- 상승 예측
        if v_price_change > 0 and v_price_change_percent >= v_prediction.target_percent then
            -- 승리: 상승했고 목표 퍼센트 달성
            v_status := 'WIN';
            -- 배당률: 0.5% → 2배, 1.0% → 3배, 1.5% → 4배, 2.0% → 5배
            v_payout := v_prediction.bet_amount * (v_prediction.target_percent * 2 + 1);
        elsif v_price_change < 0 then
            -- 패배: 하락함
            v_status := 'LOSS';
            v_payout := 0;
        else
            -- 무승부: 상승했지만 목표 미달
            v_status := 'ND';
            v_payout := v_prediction.bet_amount; -- 베팅액 반환
        end if;
        
    elsif v_prediction.direction = 'DOWN' then
        -- 하락 예측
        if v_price_change < 0 and v_price_change_percent >= v_prediction.target_percent then
            -- 승리: 하락했고 목표 퍼센트 달성
            v_status := 'WIN';
            v_payout := v_prediction.bet_amount * (v_prediction.target_percent * 2 + 1);
        elsif v_price_change > 0 then
            -- 패배: 상승함
            v_status := 'LOSS';
            v_payout := 0;
        else
            -- 무승부: 하락했지만 목표 미달
            v_status := 'ND';
            v_payout := v_prediction.bet_amount;
        end if;
    end if;
    
    -- 4. 예측 상태 업데이트
    update predictions
    set 
        status = v_status,
        actual_price = p_close_price,
        profit = v_payout - v_prediction.bet_amount,
        resolved_at = now()
    where id = p_id;
    
    -- 5. 포인트 지급 (Atomic 트랜잭션)
    if v_payout > 0 then
        -- profiles 테이블이 없으면 에러 날 수 있으니 체크 필요하지만, 
        -- schema.sql에 의해 있다고 가정.
        -- user_id가 profiles.id와 매핑된다고 가정 (handle_new_user 트리거 등)
        update profiles
        set points = points + v_payout
        where id = v_prediction.user_id;
    end if;
    
    -- 6. 결과 반환
    v_result := json_build_object(
        'success', true,
        'status', v_status,
        'payout', v_payout,
        'profit', v_payout - v_prediction.bet_amount,
        'price_change_percent', v_price_change_percent
    );
    
    return v_result;
    
exception
    when others then
        return json_build_object(
            'success', false,
            'error', SQLERRM
        );
end;
$$;
